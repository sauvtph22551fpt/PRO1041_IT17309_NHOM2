    public KhachHang getKhachHang() {

        String maKh = txtmaKhachHang.getText();
        String tenKh = txtTenKhachHang.getText();
        String diaChi = txtDiaChi.getText();
        int sdt = Integer.valueOf(txtSoDienThoai.getText());
        return new KhachHang(null, maKh, tenKh, diaChi, sdt);
    }

    public boolean checkMaKh(String ma) {
        boolean check = true;
        for (KhachHang kh : banHangServices.getListKhachHang()) {
            if (kh.getMaKH().equalsIgnoreCase(ma)) {
                check = false;
            }
        }

        return check;
    }
    public void setKhachHang(int sdt){
        for (KhachHang kh : banHangServices.getListKhachHang()) {
            if (kh.getSdt() == sdt) {
                txtmaKhachHang.setText(kh.getMaKH());
                txtTenKhachHang.setText(kh.getTenKH());
                txtDiaChi.setText(kh.getDiaChi());
                txtSoDienThoai.setText(String.valueOf(kh.getSdt()));
            }
        }
    }

