    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        String dinhDangSDT = "^(0|\\+84)(\\s|\\.)?((3[2-9])|(5[689])|(7[06-9])|(8[1-689])"
                + "           |(9[0-46-9]))(\\d)(\\s|\\.)?(\\d{3})(\\s|\\.)?(\\d{3})$";

        if (txtmaKhachHang.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Chưa Nhập Mã Khách Hàng");
            return;
        }
        
        if (txtTenKhachHang.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Chưa Nhập Tên Khách Hàng");
            return;
        }
        if (txtDiaChi.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Chưa Nhập Địa Chỉ");
            return;
        }
        if (txtSoDienThoai.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Chưa Nhập Số Điện Thoại");
            return;
        }
        int checkTrungSDT = Integer.parseInt(txtSoDienThoai.getText());
//        int checkTrungSdt = 0 +""+checkTrungSDT
        if (banHangServices.checkTrungSdt(checkTrungSDT)) {
            JOptionPane.showMessageDialog(this, "Số Điện Thoại Đã Tồn Tại");
            return;
        }

        boolean checkSDT = txtSoDienThoai.getText().matches(dinhDangSDT);
        if (checkSDT == false) {
            JOptionPane.showMessageDialog(this, "Số Điên Thoại Không Đúng Định Dạng");
            return;
        }

        int comfirm = JOptionPane.showConfirmDialog(this, "Chắc Chắn Muốn Thêm Khách Hàng \n Tên Khách Hàng :" + txtTenKhachHang.getText() +
                "\nSố Điện Thoai:"+ txtSoDienThoai.getText()+ "\nĐịa Chỉ:"+ txtDiaChi.getText());
        if (comfirm == 0) {
            KhachHang kh = getKhachHang();
            if (checkMaKh(kh.getMaKH())) {
                if (banHangServices.themKhachHang(kh)) {
                    JOptionPane.showMessageDialog(this, "Them thanh cong");
                    txtDiaChi.setText("");
                    txtSoDienThoai.setText("");
                    txtTenKhachHang.setText("");
                    txtmaKhachHang.setText("");
                } else {
                    JOptionPane.showMessageDialog(this, "Them thất bại");
                }
            } else {
                JOptionPane.showMessageDialog(this, "Them thất bại. Mã Khách Hàng Đã Tồn Tại");
            }
        }else{
            JOptionPane.showMessageDialog(this, "Bạn Đã Xác Nhận Hủy Thêm");
        }

    }//GEN-LAST:event_btnThemActionPerformed

